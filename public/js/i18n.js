    var STORAGE_KEY = 'lumiskin-lang';
    var current = 'en';

    function apply(lang) {
        if (!T[lang]) lang = 'en';
        current = lang;
        var t = T[lang];
        var rtl = (lang === 'ar');

        document.documentElement.setAttribute('lang', lang);
        document.documentElement.setAttribute('dir', rtl ? 'rtl' : 'ltr');

        document.querySelectorAll('[data-i18n]').forEach(function (el) {
            var v = t[el.getAttribute('data-i18n')];
            if (v !== undefined) el.textContent = v;
        });

        document.querySelectorAll('[data-i18n-ph], [data-i18n-placeholder]').forEach(function (el) {
            var key = el.getAttribute('data-i18n-ph') || el.getAttribute('data-i18n-placeholder');
            var v = t[key];
            if (v !== undefined) el.setAttribute('placeholder', v);
        });

        var btn = document.getElementById('lang-switcher');
        if (btn) btn.textContent = rtl ? 'EN' : 'ع';

        var btnMobile = document.getElementById('lang-switcher-mobile');
        if (btnMobile) btnMobile.textContent = rtl ? 'EN' : 'ع';

        if (rtl && !document.getElementById('arabic-font')) {
            var lk = document.createElement('link');
            lk.id = 'arabic-font';
            lk.rel = 'stylesheet';
            lk.href = 'https://fonts.googleapis.com/css2?family=Cairo:wght@400;500;600;700&display=swap';
            document.head.appendChild(lk);
        }

        localStorage.setItem(STORAGE_KEY, lang);
        document.dispatchEvent(new CustomEvent('langchange', { detail: { lang: lang } }));
    }

    function toggle() { apply(current === 'en' ? 'ar' : 'en'); }

    function injectBtn() {
        var existing = document.getElementById('lang-switcher');
        if (existing) { existing.onclick = toggle; return; }
        var btn = document.createElement('button');
        btn.id = 'lang-switcher';
        btn.className = 'btn-secondary';
        btn.textContent = 'ع';
        btn.setAttribute('aria-label', 'Toggle language');
        btn.onclick = toggle;
        var nav = document.querySelector('header .nav-buttons');
        var theme = document.getElementById('theme-switcher-control');
        if (nav && theme) nav.insertBefore(btn, theme);
        else if (nav) nav.appendChild(btn);
    }

    function init() {
        injectBtn();
        var saved = localStorage.getItem(STORAGE_KEY);
        apply(T[saved] ? saved : 'en');
    }

    window.LumiI18n = {
        toggle: toggle,
        apply: apply,
        lang: function () { return current; },
        t: function (key) { return (T[current] && T[current][key]) || key; }
    };

    if (document.readyState === 'loading') {
        document.addEventListener('DOMContentLoaded', init);
    } else {
        init();
    }

